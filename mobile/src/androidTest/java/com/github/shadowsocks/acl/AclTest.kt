        private const val INPUT1 = """[proxy_all]
[bypass_list]
1.0.1.0/24
(^|\.)4tern\.com${'$'}
"""
    }

    @Test
    fun parse() {
        Assert.assertEquals(INPUT1, Acl().fromReader(INPUT1.reader()).toString());
    }
}
